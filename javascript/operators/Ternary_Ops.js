// // 6. Ternary Operator
// // The ternary operator is a concise way to write an if-else statement.
// // Syntax:
// // condition ? expressionIfTrue : expressionIfFalse

// let a = 5;
// let b = 3;

// // Example 1: Check if 'a' is greater than 'b', if true return 'a', else return 'b'
// let k = (a > b) ? a : b;
// console.log(k); // Output: 5

// // Example 2: Check if 'a' is less than 'b'
// let l = (a < b) ? a : b;
// console.log(l); // Output: 3

// // Example 3: Check if 'a' is equal to 'b' (loose equality)
// let m = (a == b) ? a : b;
// console.log(m); // Output: 3

// // Example 4: Check if 'a' is not equal to 'b' (loose inequality)
// let n = (a != b) ? a : b;
// console.log(n); // Output: 5

// // Example 5: Check if 'a' is strictly equal to 'b' (both value and type)
// let o = (a === b) ? a : b;
// console.log(o); // Output: 3

// // Example 6: Check if 'a' is strictly not equal to 'b'
// let p = (a !== b) ? a : b;
// console.log(p); // Output: 5

// notes
// The condition before the ? is evaluated.
// If true, the expression immediately after ? runs.
// If false, the expression after: runs.
